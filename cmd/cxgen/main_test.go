const cxPkg = "example.com/cx"

func TestFindProviders_DetectsProvider(t *testing.T) {
	root := makeModule(t, "example.com/app", map[string]string{
		"svc/svc.go": `package svc

import _ "example.com/cx"

func init() {}
`,
	})
	providers, err := findProviders(root, "./...", cxPkg)
	if err != nil {
		t.Fatal(err)
	}
	if len(providers) != 1 || providers[0] != "example.com/app/svc" {
		t.Fatalf("want [example.com/app/svc], got %v", providers)
	}
}

func TestFindProviders_IgnoresNoInit(t *testing.T) {
	root := makeModule(t, "example.com/app", map[string]string{
		"svc/svc.go": `package svc

import _ "example.com/cx"
`,
	})
	providers, err := findProviders(root, "./...", cxPkg)
	if err != nil {
		t.Fatal(err)
	}
	if len(providers) != 0 {
		t.Fatalf("expected no providers, got %v", providers)
	}
}

