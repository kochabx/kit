// writeImports tests
// ---------------------------------------------------------------------------

func TestWriteImports_NoImports(t *testing.T) {
	out := filepath.Join(t.TempDir(), "out.go")
	if err := writeImports(out, "main", nil); err != nil {
		t.Fatal(err)
	}
	data, _ := os.ReadFile(out)
	content := string(data)
	if !strings.Contains(content, "package main") {
		t.Fatal("missing package declaration")
	}
	if strings.Contains(content, "import") {
		t.Fatal("should not contain import block when there are no imports")
	}
}

func TestWriteImports_WithImports(t *testing.T) {
	out := filepath.Join(t.TempDir(), "out.go")
	if err := writeImports(out, "mypkg", []string{"example.com/a", "example.com/b"}); err != nil {
		t.Fatal(err)
	}
	data, _ := os.ReadFile(out)
	content := string(data)
	if !strings.Contains(content, "package mypkg") {
		t.Fatal("wrong package name")
	}
	if !strings.Contains(content, `_ "example.com/a"`) {
		t.Fatal("missing import a")
	}
	if !strings.Contains(content, `_ "example.com/b"`) {
		t.Fatal("missing import b")
	}
	if !strings.Contains(content, "Code generated by cxgen") {
		t.Fatal("missing generated header")
	}
}

func TestWriteImports_CreatesDir(t *testing.T) {
	out := filepath.Join(t.TempDir(), "sub", "dir", "out.go")
	if err := writeImports(out, "main", nil); err != nil {
		t.Fatal(err)
	}
	if _, err := os.Stat(out); err != nil {
		t.Fatalf("output file not created: %v", err)
	}
}

