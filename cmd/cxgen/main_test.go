func TestFindProviders_MultiplePackages(t *testing.T) {
	root := makeModule(t, "example.com/app", map[string]string{
		"a/a.go": `package a

import _ "example.com/cx"

func init() {}
`,
		"b/b.go": `package b

import _ "example.com/cx"

func init() {}
`,
	})
	providers, err := findProviders(root, "./...", cxPkg)
	if err != nil {
		t.Fatal(err)
	}
	if len(providers) != 2 {
		t.Fatalf("want 2 providers, got %v", providers)
	}
}

func TestFindProviders_SkipsCxPackageItself(t *testing.T) {
	root := makeModule(t, "example.com/cx", map[string]string{
		"cx.go": `package cx

func init() {}
`,
	})
	providers, err := findProviders(root, "./...", cxPkg)
	if err != nil {
		t.Fatal(err)
	}
	if len(providers) != 0 {
		t.Fatalf("cx package itself should be skipped, got %v", providers)
	}
}
