package main

import (
	"os"
	"os/exec"
	"path/filepath"
	"strings"
	"testing"
)

// makeModule creates a temporary Go module with sub-packages described by
// pkgs (map[relativeDir]fileContent) and returns the root directory.
// If any package imports cxPkg, a local cx stub is created and a replace
// directive is added so go mod tidy succeeds without network access.
func makeModule(t *testing.T, modName string, pkgs map[string]string) string {
	t.Helper()
	root := t.TempDir()

	// Check if any source file imports the cx stub package.
	needsCxStub := false
	for _, src := range pkgs {
		if strings.Contains(src, cxPkg) {
			needsCxStub = true
			break
		}
	}

	// go.mod — add replace directive for the cx stub if needed.
	var gomod strings.Builder
	gomod.WriteString("module " + modName + "\n\ngo 1.21\n")
	if needsCxStub && modName != cxPkg {
		// Create a standalone cx stub module next to the test module.
		cxRoot := filepath.Join(filepath.Dir(root), "cxstub")
		if err := os.MkdirAll(cxRoot, 0o755); err != nil {
			t.Fatal(err)
		}
		if err := os.WriteFile(filepath.Join(cxRoot, "go.mod"),
			[]byte("module "+cxPkg+"\n\ngo 1.21\n"), 0o644); err != nil {
			t.Fatal(err)
		}
		if err := os.WriteFile(filepath.Join(cxRoot, "cx.go"),
			[]byte("package cx\n"), 0o644); err != nil {
			t.Fatal(err)
		}
		gomod.WriteString("\nrequire " + cxPkg + " v0.0.0\n")
		gomod.WriteString("replace " + cxPkg + " => " + cxRoot + "\n")
	}
	if err := os.WriteFile(filepath.Join(root, "go.mod"), []byte(gomod.String()), 0o644); err != nil {
		t.Fatal(err)
	}

	for rel, src := range pkgs {
		dir := filepath.Join(root, filepath.Dir(rel))
		if err := os.MkdirAll(dir, 0o755); err != nil {
			t.Fatal(err)
		}
		if err := os.WriteFile(filepath.Join(root, rel), []byte(src), 0o644); err != nil {
			t.Fatal(err)
		}
	}

	// Run go mod tidy so the module is valid for packages.Load.
	cmd := exec.Command("go", "mod", "tidy")
	cmd.Dir = root
	if out, err := cmd.CombinedOutput(); err != nil {
		t.Fatalf("go mod tidy: %v\n%s", err, out)
	}
	return root
}

